@pytest.mark.parametrize("title, description", RECIPE_APPROVED_CASES)
def test_recipe_approved_cases(title, description):
    assert moderate_recipe(title, description) in {"approved", "pending"}


@pytest.mark.parametrize("title, description", RECIPE_REJECTED_CASES)
def test_recipe_rejected_cases(title, description):
    assert moderate_recipe(title, description) == "rejected"


@pytest.mark.parametrize("text", COMMENT_APPROVED_CASES)
def test_comment_approved_cases(text):
    assert moderate_comment(text) == "approved"


@pytest.mark.parametrize("text", COMMENT_REJECTED_CASES)
def test_comment_rejected_cases(text):
    assert moderate_comment(text) == "rejected"


