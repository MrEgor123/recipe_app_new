# Очень грубая локальная проверка на мусор.
GARBAGE_PATTERNS = [
    r"^[a-zа-яё]{1,2}$",
    r"^[0-9\s\W_]+$",
    r"^(.)\1{5,}$",
    r"^(asdf|qwerty|фывфыв|йцукен|тест|test|123123)$",
]


def _clean_text(text: str) -> str:
    return (text or "").strip()


def _compact_text(text: str) -> str:
    """
    Упрощённая нормализация для поиска обходов:
    - нижний регистр;
    - замены похожих символов;
    - удаление лишних разделителей.
    """
    text = (text or "").lower()

    replacements = {
        "ё": "е",
        "0": "о",
        "3": "з",
        "4": "ч",
        "6": "б",
        "8": "в",
        "@": "а",
        "$": "с",
        "a": "а",
        "e": "е",
        "o": "о",
        "p": "р",
        "c": "с",
        "x": "х",
        "y": "у",
        "k": "к",
        "m": "м",
        "t": "т",
        "b": "в",
        "h": "н",
    }

    for src, dst in replacements.items():
        text = text.replace(src, dst)

    text = re.sub(r"[\s\W_]+", "", text, flags=re.IGNORECASE)
    return text


def _looks_like_garbage(text: str) -> bool:
    text = _clean_text(text)
    lowered = text.lower()

    if not text:
        return True

    if len(text) < 3:
        return True

    for pattern in GARBAGE_PATTERNS:
        if re.search(pattern, lowered, flags=re.IGNORECASE):
            return True

    # Слишком много повторов одного символа.
    compact = _compact_text(text)
    if len(compact) >= 6 and len(set(compact)) <= 2:
        return True

    # Почти нет букв — обычно мусор.
    letters = re.findall(r"[a-zа-яё]", lowered, flags=re.IGNORECASE)
    if len(text) >= 8 and len(letters) < 3:
        return True

    return False


def _local_block_check(text: str) -> bool:
    """
    True = сразу rejected.
    """
    text = _clean_text(text)
    lowered = text.lower()
    compact = _compact_text(text)

    if _looks_like_garbage(text):
        return True

    for pattern in BLOCK_PATTERNS:
        if re.search(pattern, lowered, flags=re.IGNORECASE):
            return True

    # Поиск части запрещённых слов после удаления пробелов/точек/символов.
    compact_bad_parts = [
        "наркот",
        "закладк",
        "оруж",
        "пистолет",
        "взрыв",
        "бомб",
        "цемент",
        "бетон",
        "унитаз",
        "туалет",
        "канализац",
        "казино",
        "ставк",
        "букмекер",
        "промокод",
        "телеграм",
        "подписывай",
    ]

    return any(part in compact for part in compact_bad_parts)


def _extract_gemini_answer(data: dict) -> str:
    try:
        candidates = data.get("candidates") or []
        if not candidates:
            return ""

        content = candidates[0].get("content") or {}
        parts = content.get("parts") or []

        texts = []
        for part in parts:
            value = part.get("text")
            if value:
                texts.append(value)

        return " ".join(texts).strip().lower()
    except Exception:
        return ""


def _normalize_moderation_answer(answer: str, default: str = REJECTED) -> str:
    """
    Fail-closed:
    любой непонятный ответ считается rejected.
    """
    normalized = (answer or "").strip().lower()

    # Разрешаем только точный ответ.
    if normalized == APPROVED:
        return APPROVED

    if normalized == REJECTED:
        return REJECTED

    # На случай если модель всё-таки добавила лишний текст.
    words = re.findall(r"\bapproved\b|\brejected\b", normalized)
    if len(words) == 1:
        return words[0]

    return default


async def _ask_gemini(prompt: str, timeout: float = 8.0) -> str:
    """
    Единая функция запроса к Gemini.
    Возвращает approved/rejected.
    Если Gemini недоступен по региону или сети — не ломаем сайт.
    """
    if not settings.gemini_api_key:
        print("NO GEMINI API KEY")
        return APPROVED

    try:
        async with httpx.AsyncClient(timeout=timeout) as client:
            response = await client.post(
                GEMINI_MODEL_URL,
                params={"key": settings.gemini_api_key},
                json={
                    "contents": [
                        {
                            "parts": [
                                {"text": prompt}
                            ]
                        }
                    ]
                },
            )

        print("GEMINI STATUS:", response.status_code)

        if response.status_code != 200:
            error_body = response.text[:1000]
            print("GEMINI ERROR BODY:", error_body)

            if "User location is not supported for the API use" in error_body:
                print("GEMINI LOCATION BLOCKED: fallback to approved")
                return APPROVED

            return REJECTED

        answer = _extract_gemini_answer(response.json())
        print("GEMINI ANSWER:", answer)

        return _normalize_moderation_answer(answer, default=REJECTED)

    except Exception as e:
        print("GEMINI ERROR:", str(e))
        return APPROVED


