def normalize_text(text: str) -> str:
    replacements = {
        "0": "о",
        "3": "е",
        "4": "а",
        "1": "и",
        "!": "и",
        "@": "а",
        "$": "с",
        "ё": "е",
        "o": "о",
        "a": "а",
        "e": "е",
        "p": "р",
        "c": "с",
        "x": "х",
        "y": "у",
        "k": "к",
        "m": "м",
        "t": "т",
        "h": "н",
        "b": "в",
    }

    text = text.lower()

    for old, new in replacements.items():
        text = text.replace(old, new)

    return text


