import httpx

from app.core.config import settings


async def ai_moderate(text: str) -> str:
    if not settings.gemini_api_key:
        print("NO GEMINI API KEY")
        return "approved"

    prompt = f"""
Ты система модерации пользовательских рецептов на кулинарном сайте.

Твоя задача — определить, можно ли публиковать рецепт.

Публиковать можно только настоящий кулинарный рецепт.

Разрешай:
- блюда;
- супы;
- салаты;
- мясо, рыбу, птицу;
- выпечку;
- десерты;
- напитки;
- соусы;
- заготовки;
- обычные тексты про приготовление еды;
- шуточные, авторские или необычные названия, если описание и ингредиенты явно связаны с едой.

Запрещай:
- всё, что не является едой или рецептом;
- бессмысленный набор букв;
- случайный текст без смысла;
- тестовые записи без рецепта;
- названия вроде "фывфыв", "ываыва", "арылфораыфолаф", "qwerty", "asdf";
- бытовые предметы;
- сантехнику;
- туалет, унитаз, ванну, раковину, канализацию;
- строительные материалы;
- цемент, бетон, песок, щебень, кирпич, краску, клей;
- технику, мебель, транспорт, электронику;
- лекарства и химикаты;
- наркотики и завуалированные названия наркотиков;
- оружие;
- насилие;
- экстремизм;
- незаконные действия;
- яды и опасные вещества;
- инструкции по созданию опасных веществ.

Особые правила:
- если название само по себе неоднозначное, оценивай описание и ингредиенты;
- если название странное или авторское, но описание и ингредиенты явно кулинарные — approved;
- если название является бессмысленным набором букв и описание не объясняет кулинарный смысл — rejected;
- если описание состоит из случайных символов, цифр или бессмысленного текста — rejected;
- "Наполеон" разрешён, если речь про торт, коржи, крем, тесто, десерт;
- "Цезарь" разрешён, если речь про салат, курицу, сухарики, соус;
- "Мимоза" разрешена, если речь про салат, рыбу, яйца, сыр;
- если название "туалет", "унитаз", "цемент", "бетон", "оружие", "наркотик" — rejected, даже если описание короткое;
- если описание не похоже на приготовление еды — rejected;
- если есть нормальные пищевые ингредиенты и действия приготовления — approved.

Примеры:
Название: Наполеон. Описание: торт с коржами и кремом. Ответ: approved
Название: Цезарь. Описание: салат с курицей, сухариками и соусом. Ответ: approved
Название: Мимоза. Описание: салат с рыбой, яйцами и сыром. Ответ: approved
Название: авторская вкусняшка. Описание: смешать творог, сахар и ягоды. Ответ: approved
Название: пельмени. Описание: тесто, фарш, варить в воде. Ответ: approved
Название: суп с мясом. Описание: сварить мясо, добавить картофель и морковь. Ответ: approved

Название: арылфораыфолаф. Описание: 123. Ответ: rejected
Название: фывфывфыв. Описание: вкусно. Ответ: rejected
Название: qwerty. Описание: тест. Ответ: rejected
Название: тест. Описание: тест. Ответ: rejected
Название: туалет. Описание: вкусный рецепт. Ответ: rejected
Название: цемент. Описание: смешать с водой. Ответ: rejected
Название: бетон. Описание: добавить песок и воду. Ответ: rejected

Ответь строго одним словом:
approved или rejected

Не добавляй объяснения.

Текст рецепта:
{text}
"""

    try:
        async with httpx.AsyncClient(timeout=15.0) as client:
            response = await client.post(
                "https://generativelanguage.googleapis.com/v1beta/models/gemma-4-31b-it:generateContent",
                params={"key": settings.gemini_api_key},
                json={
                    "contents": [
                        {
                            "parts": [
                                {"text": prompt}
                            ]
                        }
                    ]
                },
            )

        print("GEMINI STATUS:", response.status_code)
        print("GEMINI RESPONSE:", response.text)

        if response.status_code != 200:
            return "approved"

        data = response.json()

        try:
            parts = data["candidates"][0]["content"]["parts"]
            answer = parts[-1]["text"].strip().lower()
        except Exception:
            return "approved"

        if "rejected" in answer:
            return "rejected"

        if "approved" in answer:
            return "approved"

        return "approved"

    except Exception as e:
        print("GEMINI ERROR:", str(e))
        return "approved"