async def ai_moderate(text: str) -> str:
    """
    Модерация рецепта.
    Строгий режим: при сомнении или ошибке rejected.
    """
    text = _clean_text(text)

    if _looks_like_garbage(text):
        print("LOCAL RECIPE MODERATION: rejected garbage")
        return REJECTED

    prompt = _recipe_prompt(text)
    return await _ask_gemini(prompt, timeout=8.0)


async def ai_moderate_comment(text: str) -> str:
    """
    Модерация комментария.
    Строгий режим: при сомнении или ошибке rejected.
    """
    text = _clean_text(text)

    if _local_block_check(text):
        print("LOCAL COMMENT MODERATION: rejected")
        return REJECTED

    prompt = _comment_prompt(text)
    return await _ask_gemini(prompt, timeout=8.0)